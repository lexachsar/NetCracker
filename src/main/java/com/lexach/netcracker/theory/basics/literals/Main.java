package com.lexach.netcracker.theory.basics.literals;


public class Main {

    public static void main(String[] args) {

        // Escape sequences

        // \b backspace
        System.out.println("AAAAAAAAAB\b");

        // \f next page
        System.out.println("First page\f");
        System.out.println("Second page");

        // \n next line
        System.out.println("First line\n");
        System.out.println("Second line");

        // \r carriage return (start of the line)
        System.out.println("AAAA\rBBBB");

        // \t tabulation
        System.out.println("Tab\tTab");

        // \\ backslash
        System.out.println("\\");

        // \â€™ apostrophe
        System.out.println("\'");

        // \" quote
        System.out.println("\"");

    }

}
